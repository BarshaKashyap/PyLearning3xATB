# Ternary Operator
barsha_marks = 90
utpal_marks = 97

#   x > y -> do something - print("pramod")
# y > x -> do something else -> print("amit)
print("barsha is winner" if barsha_marks > utpal_marks else "utpal is winner")


if barsha_marks > utpal_marks:
    print("barsha is the winner")
else:
    print("utpal is the winner")